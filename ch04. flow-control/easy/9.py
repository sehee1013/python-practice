# 컴퓨터는 "바위" 고정. 사용자 입력과 비교하여 결과를 출력하세요.
computer = "바위"
user = input()

# 컴퓨터의 바위 출력
print(f"컴퓨터: {computer}")

# 사용자가 바위인 경우, "나: 바위", "결과: 비겼습니다" 출력
if user == "바위":
    print(f"나: {user}")
    print("결과: 비겼습니다")
# 사용자가 가위인 경우, "나: 가위", "결과: 졌습니다" 출력
elif user == "가위":
    print(f"나: {user}")
    print("결과: 졌습니다")
# 사용자가 보인 경우, "나: 보", "결과: 이겼습니다" 출력
elif user == "보":
    print(f"나: {user}")
    print("결과: 이겼습니다")
# 그 외, "잘못된 입력입니다" 출력
else:
    print("잘못된 입력입니다")